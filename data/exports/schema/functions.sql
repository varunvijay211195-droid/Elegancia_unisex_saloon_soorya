CREATE OR REPLACE FUNCTION public.redeem_loyalty_points(p_customer_id uuid, p_points integer, p_description text DEFAULT NULL::text)
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_available_points INTEGER;
  v_transaction_id UUID;
